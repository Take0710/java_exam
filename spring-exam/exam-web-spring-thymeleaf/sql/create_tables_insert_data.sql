--チーム
DROP TABLE IF EXISTS teams;
CREATE TABLE teams (id serial primary key
		  ,name text NOT NULL UNIQUE
		  ,stadium_id integer NOT NULL
		  ,established_date date NOT NULL
		  );
--本拠地
DROP TABLE IF EXISTS stadiums;
CREATE TABLE stadiums (id serial primary key
		  ,name text NOT NULL UNIQUE
		  ,prefecure_code text NOT NULL
		  );
--都道府県
DROP TABLE IF EXISTS prefectures;
CREATE TABLE prefectures (id serial primary key
		  ,prefecure_code text NOT NULL UNIQUE
		  ,name text NOT NULL 
		  );
--選手
DROP TABLE IF EXISTS players;  
CREATE TABLE players (
		id serial primary key
		,full_name text NOT NULL UNIQUE
		,last_name text NOT NULL 
		,first_name text NOT NULL 
		,team_id int NOT NULL
		,position int NOT NULL
		,birthday date NOT NULL
		,age int NOT NULL
		,belonging_year int NOT NULL
		,height int NOT NULL
		,weight int NOT NULL
		,blood_type text NOT NULL
		,throws text NOT NULL
		,bats text NOT NULL
		,hometown text NOT NULL
		,annual_salary text NOT NULL
				  );
--ポジション
DROP TABLE IF EXISTS position;
CREATE TABLE position (id serial primary key
		      ,position_name text NOT NULL UNIQUE
);



